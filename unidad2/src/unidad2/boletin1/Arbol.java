package unidad2.boletin1;

import java.util.Scanner;


public class Arbol {

	public static void main(String[] args) {
		String cadena = " ";
		for (int i = 1; i<= 1;) {
			cadena = cadena + cadena + "*";
			System.out.println(cadena);
			i++;
			for (int z = 1; z<= 1;) {
				cadena = cadena+ "*" + " ";
				System.out.println(cadena);
				z++;
			for (int j = 2; j<= 2;) {
				cadena = "*" + cadena;
				System.out.println(cadena);
				j++;
		}
		
		}

	}

	}
}